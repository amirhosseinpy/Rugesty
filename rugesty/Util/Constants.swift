struct Fonts {
    static func iranSansBold(_ size: Float) -> UIFont {
        return UIFont(name: "IRANSansMobile-Bold", size: CGFloat(size))!
    }
    
    static func iranSansLight(_ size: Float) -> UIFont {
        return UIFont(name: "IRANSansMobile-Light", size: CGFloat(size))!
    }
    
    static func iranSansMedium(_ size: Float) -> UIFont {
        return UIFont(name: "IRANSansMobile-Medium", size: CGFloat(size))!
    }
    
    static func iranSans(_ size: Float) -> UIFont {
        return UIFont(name: "IRANSansMobile", size: CGFloat(size))!
    }
}
